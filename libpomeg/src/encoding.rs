pub fn slice_to_string(trainer_name: &[u8]) -> String {
    let mut name = String::new();

    for b in trainer_name.iter() {
        if *b == u8::MAX {
            return name;
        }
        name += INTERNATIONAL_ENCODING[*b as usize];
    }

    name
}
